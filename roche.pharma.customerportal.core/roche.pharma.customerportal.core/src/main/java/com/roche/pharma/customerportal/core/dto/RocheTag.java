package com.roche.pharma.customerportal.core.dto;

public class RocheTag {
    
    private String id;
    private String path;
    private String nodeLevel;
    private String title;
    private String title_fr;
    
    /**
     * @return the id
     */
    public String getId() {
        return id;
    }
    
    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }
    
    /**
     * @return the path
     */
    public String getPath() {
        return path;
    }
    
    /**
     * @param path the path to set
     */
    public void setPath(String path) {
        this.path = path;
    }
    
    /**
     * @return the nodeLevel
     */
    public String getNodeLevel() {
        return nodeLevel;
    }
    
    /**
     * @param nodeLevel the nodeLevel to set
     */
    public void setNodeLevel(String nodeLevel) {
        this.nodeLevel = nodeLevel;
    }
    
    /**
     * @return the title
     */
    public String getTitle() {
        return title;
    }
    
    /**
     * @param title the title to set
     */
    public void setTitle(String title) {
        this.title = title;
    }
    
    /**
     * @return the title_fr
     */
    public String getTitle_fr() {
        return title_fr;
    }
    
    /**
     * @param title_fr the title_fr to set
     */
    public void setTitle_fr(String title_fr) {
        this.title_fr = title_fr;
    }
    
}
